export type erc20EventArgs = erc20ApprovalEventArgs | erc20TransferEventArgs;

export enum erc20Events {
    Approval = 'Approval',
    Transfer = 'Transfer',
}

export interface erc20ApprovalEventArgs extends DecodedLogArgs {
    owner: string;
    spender: string;
    value: BigNumber;
}

